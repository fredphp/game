export function getAllHeroes(): Hero[] {
  const existingIds = new Set(EXISTING_HEROES.map(h => h.id));
  const newHeroes = RAW_HEROES
    .filter(entry => !existingIds.has(entry[0]))
    .map(entry => generateHero(entry));
  return [...EXISTING_HEROES, ...newHeroes];
}

export function getHeroesByFaction(faction: Faction): Hero[] {
  return getAllHeroes().filter(h => h.faction === faction);
}

export function getHeroesByRarity(rarity: Rarity): Hero[] {
  return getAllHeroes().filter(h => h.rarity === rarity);
}

export function getHeroById(id: string): Hero | undefined {
  return getAllHeroes().find(h => h.id === id);
}

export function getHeroStats() {
  const all = getAllHeroes();
  return {
    total: all.length,
    byFaction: Object.entries(
      all.reduce((acc, h) => { acc[h.faction] = (acc[h.faction] || 0) + 1; return acc; }, {} as Record<string, number>)
    ),
    byRarity: Object.entries(
      all.reduce((acc, h) => { acc[h.rarity] = (acc[h.rarity] || 0) + 1; return acc; }, {} as Record<string, number>)
    ),
    byRole: Object.entries(
      all.reduce((acc, h) => { acc[h.role] = (acc[h.role] || 0) + 1; return acc; }, {} as Record<string, number>)
    ),
  };
}
