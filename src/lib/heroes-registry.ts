function rand(min: number, max: number): number {
  return Math.floor(Math.random() * (max - min + 1)) + min;
}

function seededRand(seed: string, min: number, max: number): number {
  let hash = 0;
  for (let i = 0; i < seed.length; i++) {
    hash = ((hash << 5) - hash) + seed.charCodeAt(i);
    hash |= 0;
  }
  hash = Math.abs(hash);
  return min + (hash % (max - min + 1));
}

function generateStats(heroId: string, rarity: Rarity) {
  const ranges = RARITY_STATS[rarity];
  return {
    atk: seededRand(heroId + 'atk', ranges.atk[0], ranges.atk[1]),
    def: seededRand(heroId + 'def', ranges.def[0], ranges.def[1]),
    hp: seededRand(heroId + 'hp', ranges.hp[0], ranges.hp[1]),
    spd: seededRand(heroId + 'spd', ranges.spd[0], ranges.spd[1]),
    crit: seededRand(heroId + 'crit', ranges.crit[0], ranges.crit[1]),
  };
}

