function generateSkills(heroId: string, role: Role, element: Element, rarity: Rarity): HeroSkill[] {
  const template = SKILL_TEMPLATES[role];
  const dmgMult = rarity === 'SSR' ? 1.5 : rarity === 'SR' ? 1.0 : 0.6;

  return template.skills.map((s, i) => ({
    id: `${heroId}_skill_${i + 1}`,
    name: s.name,
    type: s.type,
    element,
    damage: s.type === 'Buff' || s.type === 'Heal' || s.type === 'Control' ? 0 :
      Math.round((s.type === 'AOE' ? 160 : s.type === 'SingleBurst' ? 320 : 120) * dmgMult * (0.8 + seededRand(heroId + 'sk' + i, 0, 40) / 100)),
    cooldown: s.type === 'Buff' ? 20 : seededRand(heroId + 'cd' + i, 8, 18),
    desc: s.desc,
    particles: s.particles,
    sound: s.sound,
    animation: s.animation,
  }));
}

